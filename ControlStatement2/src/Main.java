public class Main {
    public static void main(String[] args) {
        int user = 18;

        switch (user){
            case 18:
                System.out.println("you are 18");
                break;
            case 19:
                System.out.println("you are 19 ");
                break;
            case 20:
                System.out.println("you are 20");
                break;
            default:
                System.out.println("you are not 18,19,20 ");
        }
        System.out.println("Hello world!");
    }
}