import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        /*
        This program demos the use of nested if else
        x=50;
         */
        int x = 20;



        if (x > 30) {
            System.out.println("x>30");
            if (x > 50) {
                System.out.println("x>50");
            } else {
                System.out.println("x<50");
            }
            }else {
            System.out.println("x<30");
        }
        }
    }
